char	*cut(char *src, char *dest);

char	*cut(char *src, char *dest)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		if (src[i] >= '0' && src[i] <= '9')
		{
			dest[i / 2] = src[i];
		}
		i++;
	}
		dest[16] = '\0';
	return (dest);
}
